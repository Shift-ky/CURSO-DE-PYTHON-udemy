


'''
Interpolação básica de strings
'''

nome = 'Robson'
valor = 1.200
variavel = 'AB'

print('Olá %s o valor da compra foi %.2f '%(nome,valor))

''' formatações que dá para fazer em f string '''

print(f'{variavel:->10}.')
